import math

ângulo = float(input('Digite o ângulo que deseja: '))
seno = math.sin(math.radians(ângulo))
print(f'O ângulo de {ângulo} tem o Seno de {seno:.2f} ')
cosseno = math.cos(math.radians(ângulo))
print(f'O ângulo de {ângulo} tem o Seno de {cosseno:.2f} ')
tangente = math.tan(math.radians(ângulo))
print(f'O ângulo de {ângulo} tem o Seno de {tangente:.2f} ')


